import { inf, ok, err, C } from '../shared.js';
import fs from 'fs';
import path from 'path';
import readline from 'readline';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

function askQuestion(question) {
  const rl = readline.createInterface({ input: process.stdin, output: process.stdout });
  return new Promise(resolve => {
    rl.question(question, ans => { rl.close(); resolve(ans.trim()); });
  });
}

const TEMPLATES_DIR = path.join(__dirname, '..', '..', '..', 'templates', 'dardcorrules');

export async function handleInitProject(opts = {}) {
  const cwd = process.cwd();
  const rulesFile = path.join(cwd, '.dardcorrules');

  if (fs.existsSync(rulesFile)) {
    console.log(`\n${C.yellow}[!]${C.reset} .dardcorrules already exists.`);
    const overwrite = await askQuestion('Overwrite? (y/N): ');
    if (overwrite.toLowerCase() !== 'y') return;
  }

  console.log(`\n${C.bold}${C.purple}Initialize Dardcor Project${C.reset}\n`);
  console.log('Select project template:');
  const templates = ['react', 'nextjs', 'node-api', 'python', 'flutter', 'rust', 'custom'];
  templates.forEach((t, i) => console.log(`  ${C.cyan}${i+1}.${C.reset} ${t}`));
  console.log('');

  const choice = await askQuestion(`Template (1-${templates.length}): `);
  const idx = parseInt(choice) - 1;
  const template = templates[idx] || 'custom';

  let content = '';
  const templateFile = path.join(TEMPLATES_DIR, `${template}.dardcorrules`);
  if (fs.existsSync(templateFile)) {
    content = fs.readFileSync(templateFile, 'utf8');
  } else {
    const projectName = path.basename(cwd);
    content = `# Dardcor Rules — ${projectName}\n# Auto-generated by dardcor init\n\n## Project\nThis is a ${template} project.\n\n## Conventions\n- Follow existing code style\n- Write tests for new features\n- Keep functions small and focused\n\n## Architecture\n- Describe your architecture here\n`;
  }

  fs.writeFileSync(rulesFile, content);
  ok('.dardcorrules created!');
  inf(`Edit it to customize agent behavior: ${rulesFile}`);
  console.log('');
}
